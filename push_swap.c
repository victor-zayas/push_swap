#include"push_swap.h"

int	main(int argc, char **argv)
{
	int	i;

	i = 0;
	while (argc > 1)
	{
		ft_atoi(argv[1]);
		write (1, &argv[i], 1);
		i++;
	}
	printf("%d\n", argv);
}
